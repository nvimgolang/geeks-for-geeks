package main

import "fmt"

func main() {
	p := 34
	q := 20

	// '=='(equal to)
	result1 := p == q
	fmt.Println(result1)

	// '!='(not equal to)
	result2 := p != q
	fmt.Println(result2)

	// '<'(less than)
	result3 := p < q
	fmt.Println(result3)

	// '>'(greater than)
	result4 := p > q
	fmt.Println(result4)

	// '>='(greater than equal to)
	result5 := p >= q
	fmt.Println(result5)

	// '<='(less than equal to)
	result6 := p <= q
	fmt.Println(result6)
}
